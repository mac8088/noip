#include <iostream>
using namespace std;
/*
 第47课，称心如意的输入
 --do while 语句 
 作业02：求5+10+15+20+25+....+200 = ? 
*/ 
int main()
{
	int i = 5, sum = 0;
	do
	{
		sum += i;
		i++;
	} while(i <= 200);
	cout << "5+10+15+20+25+....+200 = " << sum << endl;
	return 0;
}

//--------------------------------
//5+10+15+20+25+....+200 = 20090
//--------------------------------
