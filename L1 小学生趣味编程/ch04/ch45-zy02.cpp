#include <iostream>
using namespace std;
/*
 第45课，蝴蝶效应 
 --作业02
 韩信带1500名士兵打仗，战死四五百人，幸存的士兵站3人一排，多出2人；
 站5人一排，多出4人；站7人一排，多出6人，算一算幸存的士兵至少有多少人？ 
*/ 
int main()
{
	int i;
	i = 1000;   //以1000为基数，试算（枚举） 
	while(true)
	{
		if(i%3==2 && i%5==4 && i%7==6) break;
		i++; 
	}
	cout << i << endl;	
	return 0;
}

