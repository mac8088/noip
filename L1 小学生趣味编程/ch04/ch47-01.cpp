#include <iostream>
using namespace std;
/*
 第47课，称心如意的输入
 --do while 语句 
   
*/ 
int main()
{
	float x;
	do
	{
		cout << "请输入成绩（0～100）：";
		cin >> x;
		
	}while(x < 0 || x > 100);
	cout << "成绩：" << x << endl;
	return 0;
}

//--------------------------------
//请输入成绩（0～100）： 2000
//请输入成绩（0～100）： -10
//请输入成绩（0～100）： 99
//成绩： 99
//--------------------------------
