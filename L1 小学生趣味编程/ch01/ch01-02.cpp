#include <iostream>
using namespace std;

/*使用流插入符 count <<，将输出流的内容输出到系统的指定设备（一般是屏幕） */
int main(){
	cout << "唐代诗人孟浩然，看孩子自编的春晓" << endl;
	cout << "春眠不觉晓，处处蚊子咬。" << "夜里嗡嗡声，脓包知多少。" << endl;
	cout << endl << endl;
	cout << endl << endl;
	cout << "春晓" << endl;
	cout << "春眠不觉晓，" << endl;
	cout << "脓包知多少。" << endl;
	cout << "夜里嗡嗡声，" << endl;
	cout << "脓包知多少。" << endl;
	return 0; 
}
