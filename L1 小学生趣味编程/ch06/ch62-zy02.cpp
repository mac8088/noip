#include <iostream>
using namespace std;
/*
 第62课，捉迷藏 
  --数组越界 
  作业02
*/ 
int main()
{
	bool a[96];
	int i,j,num=0;
	for(i=1;i<=96;i++)
		a[i] = false;    //先把所有的门都关上 
		
	for(i=1;i<=42;i++)
		for(j=i;j<=96;j=j+i)   //j=j+i , 动态， 自增1，自增2，3，4 
			a[j]=!a[j];
	
	for(i=1;i<=96;i++)
	{
		if(a[i])
		{
			num++;
			cout << i << endl;
		}	
	}
	cout << "共有 " << num << "扇" << endl; 
	
	return 0;
}

