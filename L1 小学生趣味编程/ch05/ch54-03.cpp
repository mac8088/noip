#include <iostream>
using namespace std;
/*
 第54课，有规律的图形 
  --循环的嵌套
  试编写一程序，在一行中输出5个 "*"号。 
*/ 
int main()
{
	int i,j;
	for(i=1;i<=3;i++)
	{
		for(j=1;j<=5;j++)
			cout << "*";
		cout << endl;
	}
	return 0;
}

