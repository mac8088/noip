#include <iostream>
using namespace std;
/*
 第54课，有规律的图形 
  --循环的嵌套
  试编写一程序，在一行中输出5个 "*"号。 
*/ 
int main()
{
	int j;
	//---1------
	for(j=1;j<=5;j++)
		cout << "*";
	cout << endl;
	//---2------
	for(j=1;j<=5;j++)
		cout << "*";
	cout << endl;
	//---3------
	for(j=1;j<=5;j++)
		cout << "*";
	cout << endl;	
	return 0;
}

