#include<iostream>
using namespace std;
int main()
{
	double h;						//h存储每次落地后弹跳的高度 
	scanf("%lf", &h);				//读入初始化的高度 
	double s=h;						//s先记录第一次落地经过的米数 
	int i=1;
	do
	{
		h/=2;						//每次落地后反弹回原来高度的一半 
		s+=h*2;						//累加反弹和接下来落地的米数 
		i++;						//累加反弹次数 
	} while(i<=9);					//反弹9次 
	printf("%g\n%g\n", s, h/2);		//最后一次反弹的高度是前一次的一半  
	return 0;
}

