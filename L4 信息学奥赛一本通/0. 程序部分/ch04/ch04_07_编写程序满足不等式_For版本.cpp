#include<iostream>
using namespace std;
//编写一程序求满足不等式 1+1/2+1/3+1/4+...+1/n >= 5的最小n值 
int main()
{
	int i = 0;
	float s = 0;
	for(i=1;s<5;i++)
	{
		s+=1.0/i;
	} 
	//for循环会多执行一次 i的自增，所以要减1 
	cout << i-1;
	return 0;
}

