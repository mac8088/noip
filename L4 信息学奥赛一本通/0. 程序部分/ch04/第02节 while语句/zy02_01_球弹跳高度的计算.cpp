#include<iostream>
using namespace std;
int main()
{
	double h,s;
	scanf("%lf", &h);
	int i = 1;
	s=h;			//第一次落地经过的米数，实数用double 
	while(i<=9)
	{
		h/=2;		//每次落地后反弹回原来高度的一半 
		s+=h*2;		//累加反弹和接下来落地的米数 
		++i;		//累加反弹次数 
	}
	//%g字段选用%f、%e格式中输出宽度较小的一种使用 
	printf("%g\n%g\n", s, h/2);		//最后一次反弹的高度是前一次的一半  
	return 0;
}

